/* USER CODE BEGIN Header_Startbutontask */
/**
* @brief Function implementing the butontask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_Startbutontask */
void Startbutontask(void const * argument)
{
  /* USER CODE BEGIN Startbutontask */
	 uint8_t last_button_state = 1;
   uint32_t debounce_time = 0;
  /* Infinite loop */
  for(;;)
  {
		 uint8_t button_state = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_9);  // Baca status tombol
        
        // Deteksi perubahan tombol dengan debounce
        if (button_state == GPIO_PIN_RESET && last_button_state == GPIO_PIN_SET && HAL_GetTick() - debounce_time > 200) {
            debounce_time = HAL_GetTick();
            // Toggle LED setiap kali tombol ditekan
            led_status = !led_status;
            
        }
        last_button_state = button_state;
    osDelay(500);
  }
  /* USER CODE END Startbutontask */
}

/* USER CODE BEGIN Header_StartTask04 */
/**
* @brief Function implementing the task04 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask04 */
void StartTask04(void const * argument)
{
  /* USER CODE BEGIN StartTask04 */
  /* Infinite loop */
  for(;;)
  {
		 HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, led_status ? GPIO_PIN_SET : GPIO_PIN_RESET);

        // Kirim status LED ke UART
        if (led_status) {
            HAL_UART_Transmit(&huart2, (uint8_t *)"LED ON\r\n", strlen("LED ON\r\n"), HAL_MAX_DELAY);
        } else {
            HAL_UART_Transmit(&huart2, (uint8_t *)"LED OFF\r\n", strlen("LED OFF\r\n"), HAL_MAX_DELAY);
        }

    osDelay(500);
  }
  /* USER CODE END StartTask04 */
}

